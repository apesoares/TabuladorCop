    public void atribui_turno(String turno) {
        try {
            int[] selected_rows = main_tab.getSelectedRows();

            if (selected_rows.length > 0) {
                Connection conn = Db_class.mysql_conn();
                for (int row : selected_rows) {

                    String login_usuario = (String) main_tab.getValueAt(row, 0);
                    String query = "UPDATE users "
                            + "SET turno = '" + turno + "' "
                            + "WHERE login = '" + login_usuario + "'";

                    Db_class.mysql_insert(query, conn);
                }

                Db_class.close_conn(conn);
                global.show_message("Atribuição de turno realizada");
                atualiza_painel();
            } else {
                global.show_message("Selecione ao menos um usuário para a troca de turno");
            }

        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException | SQLException ex) {
            Logger.getLogger(div_turno.class.getName()).log(Level.SEVERE, null, ex);
            global.show_error_message(
                    "Problemas com a troca de turno.\n\n"
                    + "Erro: " + ex.getMessage()
            );
        }

    }

