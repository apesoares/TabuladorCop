    public void atualiza_painel() {

        jPanel1.removeAll();
        jPanel1.repaint();
        jPanel1.revalidate();

        String query = "SELECT login, ifnull(nome, '') as Nome, ifnull(turno,'') as Turno "
                + "FROM users "
                + "WHERE cop = '" + mn.get_cop() + "' "
                + "and perfil = 2 "
                + "ORDER BY nome";
        try {

            JTable tab = global.getTable(query, jPanel1);

            int invisible_ids[] = {0};
            int column_widths[] = {0, 150, 150};

            global.hide_columns(invisible_ids, tab);
            global.adjust_columns(column_widths, tab);

            main_tab = tab;

            TableFilterHeader filter = new TableFilterHeader(main_tab, AutoChoices.ENABLED);
            filter.setAdaptiveChoices(true);

        } catch (Exception ex) {
            Logger.getLogger(div_turno.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

